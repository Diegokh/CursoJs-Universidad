//Sentencia if

let miNumero = 1;

//Revisar si el numero es positivo

if(miNumero>0){
    console.log(`El numero es positivo ${miNumero}`)
}else{
    console.log(`El numero es negativo o es cero ${miNumero}`)
}

//Operador ternario

(miNumero  > 0) ? console.log('Positivo') : console.log('Negativo')